
$("#card-dec").delegate(".bookadd","click",function(){
    $.ajax({
        type:"POST",
        url:"/content",
        data:{"bname":$(this).data("id"),"bimg":$(this).data("img")},
        success:function(data){
            alert("添加成功！可以在我的书架阅读查看图书啦！")
    }
  })
  })
  $("#bookcard").delegate(".bookrack_del","click",function(){
    $.ajax({
         type:"POST",
         url:"/content/mybookrack_del",
         data:{"bname":$(this).data("i"),"bimg":$(this).data("d")},
         success:function(data){
           if(data.status==1){
             window.location.href="/mybookrack";
         }
     }
   })
   })